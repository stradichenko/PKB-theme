document.addEventListener('DOMContentLoaded', function () {
  const searchInput = document.querySelector('.search-container input[type="search"]');
  const clearIcon = document.querySelector('.clear-icon');

  if (searchInput && clearIcon) {
    // Show/hide the clear icon based on input value
    searchInput.addEventListener('input', function () {
      clearIcon.style.display = searchInput.value.length > 0 ? 'block' : 'none';
    });

    // Clear input when clear icon is clicked
    clearIcon.addEventListener('click', function () {
      searchInput.value = '';
      clearIcon.style.display = 'none';
      searchInput.focus();
    });
  }
});
